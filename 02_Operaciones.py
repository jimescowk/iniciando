print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 3)
print(10 // 3)
print(2 **3)

print("hola" + " Python")  # solo es mas funciona para texto, si todo es texto o todo numerico
print("Hola " + str(5))  # se puede mezclar texto y numeros, si los numeros se convierten a texto
